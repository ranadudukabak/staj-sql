--QUERY THAT RETURNS THE MAXIMUM BONUS AMOUNT OF AN EXECUTIVE WORKER

SELECT MAX(BONUS_AMOUNT) AS EXECUTIVE_MAX_BONUS_AMOUNT
FROM Bonus AS B
INNER JOIN Title AS T
ON B.WORKER_REF_ID = T.WORKER_REF_ID
WHERE T.WORKER_TITLE = 'Executive' AND YEAR(BONUS_DATE) = 2016

