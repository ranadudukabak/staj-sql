--QUERY TO PRINT THE NAME OF EACH EMPLOYEES HAVING THE HIGHEST SALARY IN EACH DEPT

SELECT CONCAT(FIRST_NAME, LAST_NAME) AS COMPLETE_NAME, SALARY 
FROM Worker 
WHERE SALARY IN (SELECT DISTINCT MAX(SALARY) FROM Worker GROUP BY DEPARTMENT)

